export const getTranslation = (key: string, language = 'en') => {
  // Select the appropriate translation object based on language
  const translations = 
    language === 'fr' ? frTranslations : 
    language === 'es' ? esTranslations : 
    language === 'ru' ? ruTranslations : 
    language === 'ar' ? arTranslations : 
    language === 'pt' ? ptTranslations : 
    language === 'tr' ? trTranslations : 
    language === 'id' ? idTranslations : 
    language === 'th' ? thTranslations : 
    language === 'hi' ? hiTranslations : 
    enTranslations;
  
  // Return the translated text for the key or fallback to English
  return translations[key as keyof typeof translations] || enTranslations[key as keyof typeof enTranslations] || key;
};
